function getResolutionTypeName(typeKey, lang) {
    for (const category of Object.values(RESOLUTION_TYPES)) {
        if (category.types[typeKey]) {
            return lang === 'kz' ? category.types[typeKey].name_kz : category.types[typeKey].name_ru;
        }
    }
    return typeKey;
}

function getCategoryName(categoryKey, lang) {
    if (RESOLUTION_TYPES[categoryKey]) {
        return lang === 'kz' ? RESOLUTION_TYPES[categoryKey].name_kz : RESOLUTION_TYPES[categoryKey].name_ru;
    }
    return categoryKey;
}

